
nilai = int(input("Masukkan nilai Anda: "))

# Menentukan kategori nilai
if nilai >= 90:
    print("Index nilai Anda adalah A")
elif nilai >= 80:
    print("Index nilai Anda adalah B")
elif nilai >= 70:
    print("Index nilai Anda adalah C")
elif nilai >= 60:
    print("Index nilai Anda adalah D")
else:
    print("Index nilai Anda adalah E")